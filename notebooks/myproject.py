WINDOW_SIZE = 20

def to_cnn_matrix_sorted(X, y, window_size=20):
    """Sort by label first so each window contains only one class."""
    sort_idx  = np.argsort(y, kind='stable')
    X_sorted  = X[sort_idx]
    y_sorted  = y[sort_idx]
    n_windows = len(X_sorted) // window_size
    X_w       = X_sorted[:n_windows * window_size].reshape(
                    n_windows, window_size, X.shape[1])
    y_w       = y_sorted[:n_windows * window_size:window_size]
    return X_w[..., np.newaxis], y_w

# ── Convert filtered data to CNN windows ──────────────────────
X_train_cnn, y_train_cnn = to_cnn_matrix_sorted(X_train_f, y_train_f, WINDOW_SIZE)
